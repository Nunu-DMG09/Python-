# Python tiene varios tipos de datos 
# type() -> Te indica que tipo de valor de datos es, increible :D
# int, float, complex, str, bool, nonetype, unic, list, tuplas, range, etc

# TIPO DE DATO INT 
# EJEMPLO: 

print("INT:")
print(type(10))
print(type(5555))
print(type(-5))
print(type(165489549))

# TIPO DE DATO FLOAT
# EJEMPLO:

print("FLOAT:")
print(type(10.5))
print(type(5.0))
print(type(-2.5))
print(type(16.5489549))

# TIPO DE DATO COMPLEX
# TIENEN NUMEROS REALES E IMAGINARIOS
# EJEMPLO:

print("COMPLEX:") 
print(type(3+2j))
print(type(-1-2j))
print(type(1+3j))

# TIPO DE DATO STR
# EJEMPLO:

print("STR:")
print(type("Hola"))
print(type("Python es genial"))
print(type('Si lo puedes so√±ar, lo puedes programar'))

# TIPO DE DATO BOOL
# EJEMPLO:

print("BOOL:")
print(type(True))
print(type(False))
print(type(1 < 2))

# TIPO DE DATO NONE 
# EJEMPLO:

print("NONE:")
print(type(None))